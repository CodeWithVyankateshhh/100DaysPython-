# # Mathematical Operators
# # + , - , * , / , ** , %
# # PEMDAS - Parentheses Exponents Multiplication Division Addition Subtraction
# # () , ** , *, / , + ,-  

# # Example
print(3 * 3 + 3 / 3 - 3)  # 7.0
print(3 * (3 + 3) / 3 - 3)  # 0.0


# # Addition
print(3 + 5)  # 8
print(-3 + 5)  # 2

# # Subtraction
print(10 - 4)  # 6
print(4 - 10)  # -6

# # Multiplication
print(4 * 2)  # 8
print(-4 * 2)  # -8


# # Exponentiation
print(2 ** 3)  # 8
print(3 ** 3)  # 27

