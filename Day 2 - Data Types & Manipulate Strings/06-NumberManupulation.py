# f-string is used to format the string with variables
# f-string is prefixed with 'f' or 'F' before the string literal and allows embedding expressions inside string literals, using curly braces {}.


# Example
name = "Vyankatesh"
age = 20
print(f"My name is {name} and I am {age} years old.")
# Output: My name is Vyankatesh and I am 20 years old.


# You can also perform operations inside the curly braces
print(f"In 5 years, I will be {age + 5} years old.")
# Output: In 5 years, I will be 25 years old.
