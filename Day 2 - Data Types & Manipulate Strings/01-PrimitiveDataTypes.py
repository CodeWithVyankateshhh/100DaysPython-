# subscripting strings
name = "Vyankatesh"
print("Vyankatesh"[0]) # accessing the first character
first_char = name[0]  # first character
last_char = name[-1]  # last character
mid_char = name[1:5]  # substring from index 1 to 4
print("First character:", first_char)
print("Last character:", last_char)
print("Middle character:", mid_char)

print("111" + "222")  # string concatenation
