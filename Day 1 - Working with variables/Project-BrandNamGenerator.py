# Project: Brand Name Generator
print("Welcome to the Brand Name Generator!")

city = input("Enter the city you grew up in:\n")
pet = input("Enter your pet's name:\n")
brand_name = city + " " + pet
print("Your brand name could be: "+ brand_name)

# This program generates a brand name by combining the name of the city where the user grew up and their pet's name.
# It uses the input() function to get user input and concatenates the strings to form the brand name.
# Finally, it prints the generated brand name to the console.