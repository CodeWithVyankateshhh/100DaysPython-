# Comparison Operators
a = 10
b = 20
print(a == b)  # Equal to
print(a != b)  # Not equal to
print(a > b)   # Greater than
print(a < b)   # Less than
print(a >= b)  # Greater than or equal to
print(a <= b)  # Less than or equal to  
print(a is b)  # Identity operator
print(a is not b)  # Not identity operator
# The comparison operators return boolean values (True or False) based on the comparison between two values.
# Example Usage
x = 15
y = 10
print(x > y)  # True
print(x < y)  # False
print(x == y) # False

